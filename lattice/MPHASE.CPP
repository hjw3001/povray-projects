#include <stdio.h>
#include <stdlib.h>

main( int argc, char *argv[] )
{
   FILE *o;

   if (argc < 1) {
      printf( "\nERROR: You need to pass in the wave phase\n" );
      exit(0);
   }

   o = fopen("phase.inc","w");

   fprintf(o, "// phase.inc\n" );
   fprintf(o, "//\n" );
   fprintf(o, "// Generated by: Henry Wagner (henryw@panix.com)\n\n" );

   fprintf(o, "#declare WAVE_PHASE = %s\n", argv[1] );

   fclose(o);
   return(0);
}
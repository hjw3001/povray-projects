#include <stdio.h>
#include <stdlib.h>

main (int argc, char *argv[] )
{
   FILE *o;

   if ( argc < 1 ) {
      printf( "\nERROR: You need to pass in the rotate parameter\n" );
      exit(0);
   }

   o = fopen("rotate.inc","w");

   fprintf(o,"// rotate.inc\n" );
   fprintf(o,"//\n" );
   fprintf(o,"// Generated by: Henry Wagner\n\n" );

   fprintf(o,"#declare R1 = %s\n", argv[1]);

   fclose(o);
   return(0);
}